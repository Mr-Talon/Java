public class Point {
    private int x=0;
    private int y=0;

    public Point(int x,int y){
        this.x=x;
        this.y=y;
    }
    public void setXAndY(int a,int b){
        this.x=a;
        this.y=b;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
}
